CREATE TABLE Employees (
    ID INT PRIMARY KEY,
    Name NVARCHAR(255),
    Supervisor NVARCHAR(255),
    HireDate DATE
);

CREATE TABLE Invoices (
    ID INT IDENTITY(1,1) PRIMARY KEY,
    EmployeeID INT,
    TotalEdits INT,
    EditTime INT,
    EditDate DATE,
    FOREIGN KEY (EmployeeID) REFERENCES Employees(ID)
);

